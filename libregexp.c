/**
 * @brief 初始化字符范围类（\d, \D, \s, \S, \w, \W）
 * @param s 解析状态
 * @param cr 输出的字符串列表（包含字符范围）
 * @param c 字符类标识符
 * @return 0=成功，-1=失败
 * 
 * 参数 c 的编码：
 * - 低 1 位：是否取反（1=取反，如 \D 是 \d 的取反）
 * - 高位部分：索引 char_range_table（0=\d, 1=\s, 2=\w）
 * 
 * 流程：
 * 1. 从预定义表中获取字符范围数据
 * 2. 将所有区间点添加到字符范围
 * 3. 如果需要取反，调用 cr_invert 进行补集运算
 */
static int cr_init_char_range(REParseState *s, REStringList *cr, uint32_t c)
{
    BOOL invert;
    const uint16_t *c_pt;
    int len, i;

    invert = c & 1;
    c_pt = char_range_table[c >> 1];
    len = *c_pt++;
    re_string_list_init(s, cr);
    for(i = 0; i < len * 2; i++) {
        if (cr_add_point(&cr->cr, c_pt[i]))
            goto fail;
    }
    if (invert) {
        if (cr_invert(&cr->cr))
            goto fail;
    }
    return 0;
 fail:
    re_string_list_free(cr);
    return -1;
}


/**
 * REParseState - 正则解析状态结构
 * 
 * 在编译阶段保存解析器的完整状态，用于递归下降解析。
 * 
 * @byte_code: 动态缓冲区，存储生成的字节码
 * @buf_ptr: 当前解析位置（指向输入正则字符串）
 * @buf_end: 输入缓冲区结束位置
 * @buf_start: 输入缓冲区起始位置
 * @re_flags: 正则标志位（全局、忽略大小写、多行等）
 * @is_unicode: 是否启用 Unicode 模式（影响字符匹配和转义序列）
 * @unicode_sets: 是否启用 Unicode 集合模式（更严格的一致性问题处理）
 * @ignore_case: 是否忽略大小写（影响字符匹配和字符类）
 * @multi_line: 多行模式（^和$匹配行首行尾而非字符串首尾）
 * @dotall: 点号匹配所有模式（. 是否匹配换行符）
 * @group_name_scope: 命名分组的当前作用域
 * @capture_count: 捕获组计数器（从 1 开始，0 保留给整个匹配）
 * @total_capture_count: 总捕获组数量（-1 表示尚未计算）
 * @has_named_captures: 是否包含命名分组（-1=未知，0=无，1=有）
 * @opaque: 用户自定义数据（传递给内存分配和中断回调）
 * @group_names: 命名分组名称表（按编号顺序存储）
 * @u: 联合体，用于错误消息或临时缓冲区
 */
typedef struct {
    DynBuf byte_code;              // 字节码动态缓冲区
    const uint8_t *buf_ptr;        // 当前解析位置
    const uint8_t *buf_end;        // 输入缓冲区结束
    const uint8_t *buf_start;      // 输入缓冲区起始
    int re_flags;                  // 正则标志位
    BOOL is_unicode;               // Unicode 模式标志
    BOOL unicode_sets;             // Unicode 集合模式标志
    BOOL ignore_case;              // 忽略大小写标志
    BOOL multi_line;               // 多行模式标志
    BOOL dotall;                   // 点号匹配所有标志
    uint8_t group_name_scope;      // 命名分组作用域
    int capture_count;             // 捕获组计数器
    int total_capture_count;       // 总捕获组数量（-1=未计算）
    int has_named_captures;        // 是否有命名分组（-1/0/1）
    void *opaque;                  // 用户自定义数据
    DynBuf group_names;            // 命名分组名称表
    union {
        char error_msg[TMP_BUF_SIZE];   // 错误消息缓冲区
        char tmp_buf[TMP_BUF_SIZE];     // 临时缓冲区
    } u;
} REParseState;

